    const ids = Array.from(selectedIds);
    for (const id of ids) {
      try {
        if (!online) throw new Error('offline');
        await fetch(`/api/rfis/${id}/status`, {
          method: 'PATCH',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ status: 'closed' }),
        });
      } catch {
        await enqueue({
          url: `/api/rfis/${id}/status`,
          method: 'PATCH',
          body: JSON.stringify({ status: 'closed' }),
          contentType: 'application/json',
          isFormData: false,
        });
      }
    }
    setRfis(prev => prev.map(r => ids.includes(r.id) ? { ...r, status: 'closed' } : r));
