        setSelectedRfi((prev) => prev ? {
          ...prev,
          responses: [...(prev.responses || []), { author: 'You (field)', text: responseText.trim(), created_at: new Date().toISOString() }],
        } : prev);
        setRfis((prev) => prev.map((r) => r.id === selectedRfi.id ? { ...r, status: 'answered' } : r));
